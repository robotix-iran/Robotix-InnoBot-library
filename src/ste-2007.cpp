//
// Set (or clear) an individual pixel
// A local copy of the frame buffer is needed since we can't
// read data from the display
//
void STE2007SetPixel(int x, int y, unsigned char ucColor)
{
    int i;
    unsigned char uc, ucOld;

    i = ((y >> 3) * 96) + x;
    if (i < 0 || i > 863) // off the screen
        return;
    uc = ucOld = ucScreen[i];
    uc &= ~(0x1 << (y & 7));
    if (ucColor)
    {
        uc |= (0x1 << (y & 7));
    }
    if (uc != ucOld) // pixel changed
    {
        STE2007SetPosition(x, y >> 3);
        STE2007WriteDataBlock(&uc, 1);
    }
} /* STE2007SetPixel() */
#endif
//
// Read the pixel at the given x,y
// if the library is not initialized, or the coordinates are
// not within the valid range, it returns 0
// Since we can't read the display memory, the pixel data
// is read from the local copy of the frame buffer
//
#ifdef BACKING_RAM
int STE2007GetPixel(int x, int y)
{
    uint8_t c = 0;
    int i = ((y >> 3) * 96) + x;
    if (i >= 0 && i <= 863) // on the screen
        c = (ucScreen[i] & (1 << (y & 7)));
    return c;
} /* STE2007GetPixel() */
#endif

//
// Invert font data
//
void InvertBytes(byte *pData, byte bLen)
{
    byte i;
    for (i = 0; i < bLen; i++)
    {
        *pData = ~(*pData);
        pData++;
    }
} /* InvertBytes() */
//
// Delays of up to 60000 us
// This is not accurate, but is good enough
// to initialize the display controller
// which allows enough time for the high/low signals to get latched
//
void delay_us(uint16_t us)
{
    while (us)
    {
        __asm__ volatile(
            "nop"
            "\n\t"
            "nop"
            "\n\t"
            "nop"
            "\n\t"
            "nop");
        us--;
    }
} /* delay_us() */
//
// Draw a string of small (6x8), normal (8x8) or large (16x16 or 16x32) characters
// At the given col+row
int STE2007WriteString(uint8_t x, uint8_t y, char *szMsg, uint8_t iSize, uint8_t bInvert)
{
    int i;
    unsigned char c, ucTemp[64];
    const unsigned char *s;

    if (iSize == FONT_LARGE) // draw 16x32 font (or stretched 8x8 if on AVR)
    {
        i = 0;
        while (x < 96 - 15 && szMsg[i] != 0)
        {

            s = &ucBigFont[(unsigned char)(szMsg[i] - 32) * 64];
            memcpy_P(ucTemp, s, 64);
            if (bInvert)
                InvertBytes(ucTemp, 64);
            STE2007SetPosition(x, y);
            STE2007WriteDataBlock(ucTemp, 16);
            STE2007SetPosition(x, y + 1);
            STE2007WriteDataBlock(&ucTemp[16], 16);
            STE2007SetPosition(x, y + 2);
            STE2007WriteDataBlock(&ucTemp[32], 16);
            STE2007SetPosition(x, y + 3);
            STE2007WriteDataBlock(&ucTemp[48], 16);

            i++;
            x += 16;
        } // while
    }
    if (iSize == FONT_NORMAL) // draw 8x8 font
    {
        STE2007SetPosition(x, y);
        i = 0;
        while (x < 96 - 7 && szMsg[i] != 0)
        {
            s = &ucFont[(unsigned char)(szMsg[i] - 32) * 8];
            memcpy_P(ucTemp, s, 8); // copy from FLASH memory
            if (bInvert)
                InvertBytes(ucTemp, 8);
            STE2007WriteDataBlock(ucTemp, 8); // write character pattern
            i++;
            x += 8;
        } // while
    }
    else if (iSize == FONT_SMALL) // 6x8
    {
        STE2007SetPosition(x, y);
        i = 0;
        while (x < 96 - 5 && szMsg[i] != 0)
        {
            s = &ucSmallFont[(unsigned char)(szMsg[i] - 32) * 6];
            memcpy_P(ucTemp, s, 6); // copy from FLASH memory
            if (bInvert)
                InvertBytes(ucTemp, 6);
            STE2007WriteDataBlock(ucTemp, 6); // write character pattern
            i++;
            x += 6;
        } // while
    }
    return 0;
} /* STE2007WriteString() */

// Fill the frame buffer with a byte pattern
// e.g. all off (0x00) or all on (0xff)
void STE2007Fill(byte ucData)
{
    int x, y;
    byte temp[32];

    memset(temp, ucData, 32);
    for (y = 0; y < 9; y++)
    {
        STE2007SetPosition(0, y); // set to (0,Y)
        for (x = 0; x < 3; x++)
        {
            STE2007WriteDataBlock(temp, 32); // fill with data byte
        } // for x
    } // for y;
} /* STE2007Fill() */
